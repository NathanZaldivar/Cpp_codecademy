#include <iostream>



int get_smallest(int num1, int num2) {
  
  return num2 < num1 ? num2 : num1; 
  
  // Value1 ? Value1 : Value 2
  
  // Logical explanation:
  // If Value1 is true then the conditional assums the value of Value1, else Value2
  
  // Basically a If else statement
  
}

double get_smallest(double num1, double num2) {
  
  return num2 < num1 ? num2 : num1;
  
}

